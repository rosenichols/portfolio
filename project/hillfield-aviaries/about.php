	<p>I have been keeping birds since September 2005, so at the time of writing I've just completed my fifth year in the hobby. The time has flown by, no pun intended.</p>
	
	<div class="caption"><img src="images/staffordsale.jpg" class="staffordsale" alt="Stafford sale" /><p class="caption">One of the sale rooms at Stafford</p></div><p>Keeping birds requires that you always look ahead to the future, be it to preparing for the cold winter, for the upcoming breeding season or the thrice annual sale and show in Stafford. In the summer, the birds have free rein of spacious outdoor flights and indoor areas. In winter, it's often a logistical nightmare to provide enough room for all adult birds and their year's resultant young inside the birdrooms. Young birds for the most part get moved on when they have reached maturity and independence. I attend sales days at Stafford two or three times a year in order to do this. There is one sale in March, one in July, and one in October. Recently, a further sale was announced for December, also at Stafford, however I have yet to attend this sale.</p>
	
	<p>I began by keeping anything I could get my hands on. I knew nothing about keeping birds when I began, and even though I read up about the subject as much as I could, and took advice from a family friend who also keeps birds, I inevitably made a lot of mistakes. But you learn quickly in a practical hobby such as this, and I had soon mastered the basics.</p>
	
	<p>I was keeping mostly Zebra Finches, which I soon came to realise are incredibly common, Bengalese (also known as Society) Finches, also common, and a smattering of Star, Bicheno, Parson and Hecks Finches; Silverbills, Java Sparrows and, incredibly, a White Headed Nun. So as you can see, it was a bit all over the place. I had yet to gain enough experience with the different birds available to me to know in which kind I wanted to specialise.</p>
	
	<p>The finches, though, believe it or not, weren't really where my heart lay anyway. To find this out, I need to take you right back to where it all began.</p>
	
	<div class="caption"><img src="images/dippyoutside.jpg" class="dippyoutside" alt="Dippy the dove" /><p class="caption">Dippy the dove, rescued and rehabilitated by me</p></div><p>I would estimate it to have been mid 2005, I remember it being quite sunny, and I had just been next door, to where my grandparents lived, to get my dinner, and I was walking down the steps to the patio and seeing a collared dove half dead, literally splattered onto the floor like something out of a cartoon. I sighed; yes I actually remember sighing, I turned and headed back to my grandparents house, popped my head around the back door, and informed the present company. My granddad came out to deal with it, and minor inconvenience to my day over, I didn't give it another thought. Well, can you see where this is going? The bird was put somewhere warm and quiet, and against all odds, it began to recover. I checked on it frequently, and I couldn't help but become attached as I aided its road to recovery. In fact, we were pretty much inseparable. It was fairly tame by now, and I delighted to fuss over it. I named it Dippy, because, being a dove, it was a bit stupid.</p>
	
	<p>Yes, I had my heart set on keeping doves. Big doves, as opposed to Diamond Doves, which are miniature and I didn't actually know existed at the time. The avicultural equivalent to our native Collared Dove is the Barbary Dove. Granted, somewhat smaller than the Collared Dove, but I had been to see a man who kept these doves and I was smitten.</p>
	
	<div class="caption"><img src="images/orangepearleddoves.jpg" class="orangepearleddoves" alt="Pair of Barbary doves" /><p class="caption">My first pair of Barbary Doves</p></div><p>From the moment the aviary had been completed, my parents had been trying to locate some of these doves for me. We eventually did so, going up to the aforementioned big sale at Stafford where we purchased a pair of the most beautiful doves you'll ever see. They were perfect, and I was so happy. I went into the aviary with them every day when I got home from school. It was to be short lived though, as less than a month later the hen suffered a prolapsed uterus and was put down under the advice of the vet. Cue much devastation.</p>
	
	<p>It wan't quite the same after that. My parents were amazing, they travelled miles to locate a replacement hen, and a few months later at the following Stafford sale, we picked up a whole load more. And yet, as I said, it just wasn't the same.</p>
	
	<p>By this point, I was gradually cutting down the number of finch species I kept and buying in birds to pair up the remaining ones. Some of the pairs had already bred, and the aviary was full of loud cheeping and much commotion. It was, and still is, an ongoing task to sell on young birds and rationalise pairs of birds that were breeding, ringing the young so as to preserve bloodlines and keep records.</p>
	
	<p>I began to specialise in Star Finches, Parson Finches and Bicheno Finches. I also had a few Diamond Doves and of course the Barbaries. I sold on the more common, beginner birds and began to cement myself in the bird keeper's network by joining an online forum, Feathered Flyer. Being young, female and quite pretty in a hobby dominated by middle aged men meant that people noticed me, and my profile attracted the highest number of views on the site, despite the fact that I didn't post very often.</p>
	
	<p>The forum was a massive help to me, and once I'd gotten used to some of the strong personalities that online forums always seem to attract, I met some amazing people who sourced birds for me that I could have never dreamed about owning otherwise, advised me on best practice, consoled me when things went wrong and celebrated my successes with me.</p>
	
	<p>From there things only grew and progressed. I now have a total of three large flights attached to two large birdrooms</p>
	
	
