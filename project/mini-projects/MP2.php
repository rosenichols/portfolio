      <h2>Choice of technique</h2>

      <p>I chose the collage method of requirements gathering. I felt this was appropriate because it would allow people to express a range of thoughts and motivations.</p>

      <h2>Implementation</h2>

      <p>I gave a lot of consideration to the method I would choose to implement this project.</p>

      <p>The first decision I made was whether to use a paper-based or digital format. I chose a digital format as it would be familiar to my demographic and within their comfort zone. I also felt to use a paper system would be s step backwards from the information age which we live in.</p>

      <p>Having made this decision, I needed to choose a method of providing the users with images. The obvious method, and indeed the one directed on the IDEO method card, was for me to choose a set of images to provide to the users for them to arrange. Although this would have been fairly straightforward for the user, having a limited scope would perhaps reduce cognitive strain, I felt this would almost certainly skew the results. Moreover, it was a concern that the limited cognitive engagement necessary to complete the task may have resulted in generic collages based on generalisations and stereotypes. I made the decision, then, to allow my users free rein of the internet’s images, through the conduit of Google’s Image Search service. In doing so, my participants would be free to choose exactly the image that fits the sentiment they were trying to express.</p>

      <p>I considered that there may be drawbacks to this method. I was mindful that there may be copyright restrictions on images pulled from internet image search services, however, as the collages themselves are in this instance not intended for public distribution, I did not consider this a cause to choose a different image repository. I was also aware that the large image scope may cause users to be overwhelmed, and get fixated by choosing just the right image, rather than focussing on what the image represents. To combat this, I considered prescribing one or more related keywords, and allowing users only to use images matched with that keyword, however I again felt this would cause either bias to my personal associations with holidays, or a lack of appropriate images due to keywords that were either too specific or generic.</p>

      <p>I chose the conference-going demographic. In the web industry, conferences are an integral part of networking and education, and they take place in various locations all around the world. I thought this group would present additional insights than those which could be gained from the average sun-seeking holiday maker. I felt it would enable me to gain a unique perspective.</p>

      <h2>Data Gathered</h2>

      <p>I approached three people to take part in this research, I chose them because they had recently travelled to attend a conference I was also at, so it would still be fresh in their minds. Although all agreed to participate, only two responses came back to me.</p>

      <p>Both collages contained 11 images. In the specification I gave them, I asked for a minimum of 10 images. The collages are contained within the Appendix.</p>

      <h2>Key Insights Gained</h2>

      <p>The key insights I gained from this exercise are as follows:</p>

      <h3>Decisions are driven by emotion</h3>

      <p>Both collages featured emotive images. Spending time with loved ones in relaxing settings is a key holiday motivation.</p>

      <p><em>Requirement:</em> Use emotive imagery to capture the user’s imagination.</p>

      <h3>Decisions are driven by logistical and practical concerns</h3>
      <p>Both collages featured images representing concerns of a practical nature, for example money (this is represented both literally, and metaphorically by the budget airline) and methods of travel.</p>

      <p><em>Requirement:</em> Provide multiple price options, and make clear what is included in the price. Allow choices of travel method. Allow sorting by price range.</p>

      <h3>Decisions are driven by aspiration</h3>
      <p>The association of holidays seems to be with luxury: eating well, opulent accommodation and exotic locations.</p>

      <p><em>Requirement:</em> Appeal to the user’s aspirations through persuasive language and images. Provide information of nearby tourist attractions. Provide images of accommodation and surrounding area.</p>


      <p>Another interesting insight was the way in which the pictures were arranged. In both collages, the images with positive connotations are featured towards the top, and those with negative connotations at the bottom. As one participant put it: ‘The things I want to think about are at the top; the things I end up thinking about are at the bottom.’</p>
