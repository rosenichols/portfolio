<h2>Who are we?</h2>

<p>Erewhon Action on Climate Change is an organisation of volunteers, with about 200 members, who are interested in raising awareness of climate change and taking action to limit its damaging effects both locally and more widely. We are a friendly group of volunteers campaigning on local, regional and national environmental campaigns.</p>

<p>We're a fast-growing and effective network of local people, businesses and organisations working together to transform Winchester into a low carbon district. We have a positive vision for the future - low energy bills, healthy lifestyles, strong communities – with a target to reduce CO2 emissions by one-third by 2015. Together we will achieve this: become a WinACC member today to play your part.</p>





<h2>What are we doing about climate change?</h2>

<p>We've been very active recently. But the threats and pressures on the environment are ever increasing. Every year there are more and more issues needing a green voice. That's why we're always eager to welcome new members and activists. As you can see from these pages, we have a huge range of activities; from fun outdoors stuff to petitioning and lobbying politicians. The group is open to people of all ages and backgrounds. Members can be involved as much or as little as they like, choosing the campaigns that interest them.</p>

<h2>How can I get involved?</h2>
<p>Feel free to drop in at one of our regular meetings to say hello and see what you think.</p>

<p>Read our newsletters to find out more about what we do. You can sign up to receive our newsletters which will keep you up to date about the campaigns we are running.</p>

