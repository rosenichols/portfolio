<h2>About Climate Change</h2>

<p>Climate Change, also known as global warming, is used to describe the changes caused by human activity - specifically, greenhouse emissions such as carbon dioxide and methane, which build up in the atmosphere and trap heat. The Intergovernmental Panel on Climate Change (IPCC) concludes that most of the observed temperature increase since the middle of the 20th century was very likely caused by increasing concentrations of greenhouse gases resulting from human activity such as fossil fuel burning and deforestation.</p>

<p>Climate model projections summarized in the latest IPCC report indicate that the global surface temperature is likely to rise a further 1.1 to 6.4 °C (2.0 to 11.5 °F) during the 21st century. An increase in global temperature will cause sea levels to rise and will change the amount and pattern of precipitation, probably including expansion of subtropical deserts.[8] Warming is expected to be strongest in the Arctic and would be associated with continuing retreat of glaciers, permafrost and sea ice. Other likely effects include changes in the frequency and intensity of extreme weather events, species extinctions, and changes in agricultural yields.</p>

