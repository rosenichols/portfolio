<h1>Carbon capture storage will 'generate 100,000 jobs and £6.5bn a year'</h1>

<img src="../images/carboncapturestorage.jpg" alt="Carbon Capture Storage" />

<p>Ed Miliband unveils strategy to encourage growth of unproven technology for next generation of coal-fired power stations</p>

<p>The UK's carbon capture and storage (CCS) sector will be able to sustain 100,000 jobs by 2030 and generate up to £6.5bn a year, the government claimed today.</p>

<p>Unveiling a new strategy to encourage the growth of CCS, the energy and climate change secretary, Ed Miliband, said it represents a "massive industrial growth opportunity".</p>

<p>The government also announced that Yorkshire and Humber had been chosen as the UK's first low-carbon economic area for CCS.</p>

<p>The region has been chosen because it combines the UK's largest cluster of industrial CO2 emitters, academic expertise and proximity to potential storage sites.</p>

<p>Yorkshire and Humber is well placed to benefit from jobs and investment that expansion in the CO2 storage industry will bring, Miliband said.</p>

<p>Announcing the new plan, the he said: "CCS presents a massive growth opportunity for the UK. We have a strong, established and skilled workforce in precisely the sectors needed to get CCS deployed at scale. And we have some of the best potential sites in Europe for CO2 storage under the North Sea."</p>

<p>Miliband added: "For the UK economy as a whole these benefits could be worth up to £6.5bn a year, sustaining jobs for up to 100,000 people, by 2030."</p>

<p>The launch of the strategy comes after two power companies were awarded funding last week to develop designs for power plants with CCS technology.</p>

<p>E.ON and Scottish Power are competing for government backing to build the UK's first CCS coal-fired power plant at either Kingsnorth, Kent or Longannet, Clackmannanshire, Scotland. The undisclosed amount of funding for each company, which is drawn from a £90m pot, will support detailed engineering and design work for the projects over the next 12 months.</p>

<p>After that, the government will announce the winner of the competition. Climate activists have predicted a planned coal station with carbon capture in Ayrshire will become the "new Kingsnorth" if it goes ahead, a reference reference to E.ON's controversial coal-fired plant in Kent that sparked battles between protesters and police before E.ON finally shelved it.</p>

<p>The Department of Energy and Climate Change (Decc) has said four coal-fired power stations which demonstrate commercial-scale CCS on a section of the plant will be built, including the winner of the competition.</p>

<p>The development of the CCS plants will potentially be funded by a fossil fuel levy on energy companies.</p>

<p>The government has pledged no new coal-fired power stations will get the go-ahead without the technology, which could potentially reduce emissions by up to 90%.</p>

<p>But climate campaigners are concerned the scheme permits construction of coal-power stations which have the technology on only part of the plant, while the rest will continue to pollute.</p>

