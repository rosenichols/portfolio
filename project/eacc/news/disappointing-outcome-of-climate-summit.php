<h1>Copenhagen climate summit undone by 'arrogance'</h1>

<p><strong>The "disappointing" outcome of December's climate summit was largely down to "arrogance" on the part of rich countries, according to Lord Stern.</strong></p>

<p>The economist told BBC News that the US and EU nations had not understood well enough the concerns of poorer nations.</p>

<p>But, he said, the summit had led to a number of countries outlining what they were prepared to do to curb emissions.</p>

<p>Seventy-three countries have now signed up to the non-binding Copenhagen Accord, the summit's outcome document.</p>

<p>The weak nature of the document led many to condemn the summit as a failure; but Lord Stern said that view was mistaken.</p>

<p>"The fact of Copenhagen and the setting of the deadline two years previously at Bali did concentrate minds, and it did lead... to quite specific plans from countries that hadn't set them out before," he said.</p>

<p>"So this process has itself been a key part of countries stating what their intentions on emissions reductions are - countries that had not stated them before, including China and the US.</p>

<p>"So that was a product of the UNFCCC (UN climate convention) process that we should respect."</p>

<p>The former World Bank chief economist and author of the influential 2006 review into the economics of climate change was speaking to BBC News following a lecture at the London School of Economics (LSE), where he now chairs the Grantham Research Institute on Climate Change and the Environment.</p>

<p>During the lecture, he compared the atmosphere at the Copenhagen summit to student politics in the 1960s - "chaotic, wearing, tiring, disappointing" - and said it was one in which countries had little room for real negotiating.</p>

<p>However, he said, it was vital to stick with the UN process, whatever its frustrations.</p>

<p><strong>Twin tracks</strong></p>

<p>Having failed to agree a treaty to supplant or supplement the Kyoto Protocol, and having failed to set a timetable for agreeing such a treaty, opinions are inevitably split on how countries seeking stronger curbs on greenhouse gas emissions should move forward.</p>

<p>Speaking in Brussels, Gro Harlem Brundtland - the UN's special envoy on climate change - suggested there would now be a twin-track approach, with some of the important discussions taking place outside the UNFCCC umbrella.</p>

<p>She also acknowledged that the talks had proved much more problematical than some governments - particularly in the EU - had anticipated.</p>

<p>"They got the message that it was much more complicated than [they had believed], and that they have to work with Brazil and China and others, not only in the broad framework of UN negotiations but also more directly and pragmatically," she said.</p>

<p>"The reality is different from half a year ago."</p>

<p>Lord Stern agreed that what he described as the "disappointing" outcome of the Copenhagen talks was largely down to rich nations' failure to understand developing world positions and concerns.</p>

<p>"[There was] less arrogance than in previous years - we have, I think, moved beyond the G8 world to the G20 world where more countries are involved - but [there was] still arrogance and it could have been much better handled by the rich countries," he said.</p>

<p>The EU limited its room for manoeuvre, he said, because too many of the leading political figures wanted to demonstrate that they were leading.</p>

<p><strong>Brass from pockets</strong></p>

<p>The most concrete part of the Copenhagen Accord is an agreement that richer countries should raise funds to help poorer nations adapt to climate impacts and "green" their economies.</p>

<p>Lord Stern is a member of the group set up by UN Secretary-General Ban Ki-moon to advise on how to raise $100bn (£66bn) per year by 2020 using various "innovative mechanisms" that could include taxes on international aviation and banking transactions.</p>

<p>But the immediate objective, he suggested, was to enact the short-term promise of providing $30bn over the period 2010-12 from the public purses of western nations.</p>

<p>If that money did not start to move fairly quickly, he said, that would further erode trust among developing countries.</p>

<p>Speaking in Brussels during a meeting with EU leaders, Mexico's environment secretary Juan Rafael Elvira endorsed the point.</p>

<p>"The developing world needs to see clear signals to have something in their hands at Cancun," he said.</p>

<p>The Mexican coastal city will host this year's UNFCCC summit.</p>

<p>"The developing countries want to see this money unblocked; the island nations especially are waiting for this funding," said Mr Elvira.</p>

<p>How and where these funds are to be disbursed has yet to be decided.</p>


