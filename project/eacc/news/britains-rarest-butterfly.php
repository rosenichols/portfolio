<h1>Downpours threaten extinction for Britain's rarest butterfly</h1>

<img src="../images/dukeofburgundy.jpg" width="600" height="310" alt="Britain's Rarest Butterfly" />
<p>Duke of Burgundy needs cowslips and rough grassland to survive after UK survey records worst ever year in 2009</p>

<p>Britain's rarest butterfly, the Duke of Burgundy, is teetering on the brink of extinction after suffering its worst summer since records began.</p>

<p>Fifty years ago this small, pugnacious insect was a common sight in English woods, but intensive farming and changing woodland management mean it is now found in significant numbers in just five countryside colonies.</p>

<p>"I'm not sure it's at a point of no return but it is in the last-chance saloon," said Martin Warren, chief executive of the charity Butterfly Conservation.</p>

<p>In the last decade the number of Duke of Burgundy colonies has fallen from 200 to fewer than 80, most of which contain a tiny number of adults that could be wiped out in a single poor summer</p>

<p>Despite a fine spring and above average summer temperatures, which brought a spectacular invasion of immigrant painted ladies, heavy rain last July and August hit many species. The next most endangered butterfly in Britain, the high brown fritillary, had its worst year, while another rarity, the pearl-bordered fritillary, had its second worst year.</p>

<p>Butterflies were counted on more than 1,000 sites across Britain by hundreds of volunteers for the UK Butterfly Monitoring Scheme run by Butterfly Conservation and the UK Centre for Ecology and Hydrology.</p>

<p>Overall, there was a small recovery for Britain's 59 butterfly species after 2008, which was the worst summer for more than 25 years.</p>

<p>Climate change is helping some species – the comma is spreading north into Scotland while the speckled wood, ringlet and green-veined white thrived in the damp but not particularly cold weather.</p>

<p>But according to Warren, the loss of suitable habitat for Duke of Burgundies and high brown fritillaries is outstripping any beneficial effect from a warming world, while the increased incidence of heavy summer rain may be killing off butterflies before they can reproduce.</p>

<p>"We've had this run of poor summers which has knocked these species down even further and they've got very little resilience to bounce back," said Warren.</p>

<p>While the Duke of Burgundy enjoyed an almost unprecedented second brood on one site in the Cotswolds last year – when adult butterflies emerged in September – this could further hit its populations as the autumn conditions are too harsh for this second brood to successfully produce offspring.</p>

<p>Lepidopterists hope conservation projects will save the high brown and pearl-bordered fritillaries but Warren said emergency management would be needed to ensure the Duke of Burgundy has the right mix of cowslips and rough grassland to survive. If not, it would become the first British butterfly to fall extinct.</p>


