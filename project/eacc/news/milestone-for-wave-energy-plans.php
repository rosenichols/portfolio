<h1>'Milestone' for wave energy plans</h1>

<img src="../images/wavepower.jpg" alt="Wave Power" />
<p>Ten sites on the seabed off the north coast of Scotland have been leased out to power companies in an effort to generate wave and tidal energy.</p>

<p>In the first project of its kind in the world, areas in the Pentland Firth and around Orkney have been leased to seven companies by the Crown Estate.</p>

<p>The companies are to push forward plans to generate enough electricity to supply 750,000 homes by 2020.</p>

<p>Scotland's First Minister Alex Salmond said it was a "major milestone".</p>

<p>He said the waters had been described as the "Saudi Arabia of marine power" due to their "rich natural resources".</p>

<p>The Crown Estate, which owns the seabed around the UK, has awarded the leases after inviting bids from developers.</p>

<p>The aim is to generate 1.2 gigawatts (GW) of electricity from the sites off Orkney and the Caithness and Sutherland coasts.</p>

<p>Six sites have been allocated for wave energy developments potentially generating 600 megawatts (MW) of power and four for tidal projects, also generating 600 MW.</p>

<p>Wave schemes will be developed by SSE Renewables Developments off Costa Head and also jointly with Aquamarine Power off Brough Head, both on Orkney.</p>

<p>ScottishPower Renewables have the lease for Marwick Head, Orkney.</p>

<p>Leases for wave projects have also been awarded to E.ON for sites designated West Orkney South and West Orkney Middle South and to Pelamis Wave Power for the Armadale site in the Pentland Firth off Sutherland.</p>

<p>Tidal schemes will be developed by SSE Renewables Developments off Westray South and jointly with OpenHydro Site Developments off Cantick Head, both Orkney.</p>

<p>Marine Current Turbines have been leased the Brough Ness site on Orkney and ScottishPower Renewables will install devices in the Pentland Firth off Ness of Duncansby in Caithness.</p>

<p>The Crown Estate has been working with the Scottish Government, Highlands and Islands Enterprise (HIE), Orkney Islands Council and Highland Council on the project.</p>

<p>The names of the successful bidders were announced at a ceremony in Edinburgh.</p>

<p>First Minister Alex Salmond said it marked a "major milestone" in efforts to create a low carbon future.</p>

<p>He said: "These waters have been described as the Saudi Arabia of marine power and the wave and tidal projects unveiled today - exceeding the initial 700MW target capacity - underline the rich natural resources of the waters off Scotland."</p>

<p>Secretary of State for Scotland Jim Murphy said it was an exciting time for the renewables industry.</p>

<p>He said: "Scotland is naturally placed to make the most of this green revolution and we will continue to work with others to ensure the potential of Scottish waters, alongside wind power, is fully met."</p>

<p>Wildlife conservation organisation, WWF Scotland, said that with careful planning wave and tidal energy could be harnessed without harming the marine environment.</p>

<p>Director Dr Richard Dixon said: "Wave and tidal power represent an energy and jobs bonanza for Scotland.</p>

<p>"There is a massive amount of power in the seas around Scotland and we are currently the world leaders in developing the technologies to turn that potential into clean, green electricity."</p>


