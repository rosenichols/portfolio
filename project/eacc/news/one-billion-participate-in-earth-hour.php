<h1>Organisers hope one billion will participate in Earth Hour</h1>

<img src="../images/earthhour.jpg" alt="Earth Hour" />
<p>OTTAWA — One billion is a big number. But that’s how many people around the world organisers are hoping will turn out their lights for Earth Hour, 2010.</p>

<p>Earth Hour is a global initiative by the World Wildlife Fund in which businesses, governments, communities and individuals are asked to turn off their lights at 8:30 p.m. local time next Saturday — March 27 — for one hour in an effort to take action on climate change.</p>

<p>Earth Hour started in 2007 in Sydney, Australia, when 2.2 million homes and businesses turned their lights off for one hour to make a statement about climate change. A year later the event had gone global.</p>

<p>Organisers hope that through their participation, Canadians and others around the world learn that it’s possible to make a positive difference for the environment.</p>

<p>This year, more than 200 cities are confirmed participants this year and 105 countries worldwide have signed on to the project.</p>

<p>“The huge success of Earth Hour shows that Canadians are ready to take action on climate change,” said Gerald Butts, president and CEO of WWF Canada.</p>

<p>“Last year, more than 10 million Canadians participated in Earth Hour, and this year we hope even more people turn off their lights, and more importantly, use that hour to think about how they can make every hour Earth Hour,” Butts said.</p>

<p>The campaign will see the world’s most famous landmarks — from the CN Tower in Toronto to the Sphinx in Egypt — go dark for one hour</p>

<p>Cities across Canada have planned a variety of events.</p>

<p>Vancouver has been participating since 2008. This year, the lights at Vancouver City Hall, Library Square and the Vancouver Museum and Space Centre will be powered down and the flood lights on the Burrard Bridge will be turned off.</p>

<p>“It’s not just about the hour,” said Amy Fournier, climate change outreach co-ordinator for the City of Vancouver.</p>

<p>“It’s about what you can learn . . . to cut down on power consumption now and every hour, and it’s a great opportunity to be part of this world event.”</p>

<p>Edmonton is holding a 2 1/2-hour event at city hall featuring local entertainment, and crafts for kids; downtown Edmonton will be shutting down its lights for the hour.</p>

<p>“There will be a countdown and a contest for schoolchildren to describe the best methods to save power,”said Mary-Ann Thurber, a spokeswoman for the City of Edmonton.</p>

<p>Montreal will see the lights turned off on all participating municipal buildings.</p>


