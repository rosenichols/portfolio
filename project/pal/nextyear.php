	<h3>A quick note on exam revision</h3>
	
	<p>One thing that my year group did for revision which worked really well was to set up a word document on Google Docs and all collaborate. This saved time and effort because each person didn't have to revise the entire syllabus for themselves, plus each person could tackle the area which they had most expertise in. It's worth a go.</p>
	
	
	
