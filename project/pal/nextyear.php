	<h3>Overview</h3>
	
	<p>I found this module a bit tricky. It is run primarily by Dan Buzzo and Prakash. Each week there's an hour lecture, and every other week a two hour tutorial session. I was timetabled for Buzzo's tutorial session, but I changed to attend Prakash's instead in order to fit in a PAL session on Friday. As long as everybody doesn't change, one or two of you could possibly switch your sessions around should it be necessary, all depending on how your timetables turn out.</p>
	
	<h3>Coursework</h3>
	
	<p>There were two pieces of coursework for this module, one in each semester. The first piece of coursework is conducted in groups. The tutors are pretty fussy about the size of the groups. You must be in a group of 3 ideally, max of 4. I ended up in a group of 4, which worked out ok, but I'd recommend that groups of 3 would've been better; the fewer people there are in a group the less potential there is for arguments. The idea of the assignment is to scrape data from the web and amalgamate several different sources into your website. It's all implicated in the title of the module really. You can <a href="resources/dsa1_spec.pdf">take a look at the spec</a> for more information. Take a look at my coursework <a href="http://www.rosenichols.co.uk/portfolio/twintowns/">here</a> and Steve's <a href="http://www.stevelacey.net/samples/weather/">here</a>.</p>
	
	<p>The coursework in the second semester is individual. The coursework has two parts.</p>
	
	<p><b>Part 1</b> - Reverse engineer a website. Sounds scary, I know because I was scared, but it's really nothing to be afraid of.</p>
	
	<p><b>Part 2</b> - Produce a spec for, or build a website which performs a similar function to the website you did the reverse engineer on. In my case, it was a website to report and locate missing things. Recommended to do the build in PHP, seeing as this is what you'll have been learning all year. See my example here - <a href="http://www.rosenichols.co.uk/portfolio/losttoys/">Lost Toys Site</a></p>
	
	<h3>Exam</h3>
	
	<p>This exam wasn't that hard. The first part is multiple choice, the second part written. You'll get a choice of questions, so you can revise your chosen topics really well. I'll see if I can dig out the paper if I even have it.</p>
	
	<h3>Key Takeaways</h3>
	
	<ul class="nextyear">
	<li>Just work through it the best you can. I realise that is rubbish advice.</li>
	
	<li>Don't make life hard for yourself. Where you are allowed to choose your own topics for coursework, keep it simple and do it well.</li>
	<li>The same as WP, this module will have a lecture session towards the end of the second semester where the exam questions will be gone through with you. Do not miss this lecture!</li>
	</ul>
	
