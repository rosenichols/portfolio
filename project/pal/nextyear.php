	<h3>Overview</h3>
	
	<p>This module was primarily run by Dan Buzzo and Michaela Reiser. I was taught by Michaela, but Dan recorded his lectures and put them online so sometimes I watched these also, particularly when elements of the coursework were being discussed. The sessions were two hours long; the first hour generally spent as a lecture and the second as a coursework workshop once the spec was given out.</p>
	
	<h3>Coursework</h3>
	
	<p>There were two pieces of coursework for this assignment.</p>
	
	<p>According to the spec, the first assignment was given out on 1st November, however it might've been a little later than that, and the deadline was 9th December. The basic premise was to construct a website from a comp image. I've talked about this further on my blog <a href="http://rosenichols.co.uk/blog/2010/12/portfolio-hillfield-aviaries-site/">here</a>.</p>
	
	<p>The scenario for the second coursework was a new folk festival being set up in Sandford (hence most people's websites - including mine - were imaginatively entitled 'Sandford Folk Festival'). This scenario will very likely change for your year. The coursework was given out on the 17th January. It was split into two parts, the deadline for the first part was 24th February and the second part 24th March.</p>
	
	<h3>Exam</h3>
	
	<p>I really didn't like this exam. It's not that the subject area is hard in itself, but it was necessary to revise twice as much information than you were ever going to need. You'll need a lot of brain space to store it all, so hope that you have a few days to revise before this exam.</p>
	
	<p>It works like this: you will be given a list of questions to revise the answers to. Twenty-four questions in all, I just counted, which in fact means that you'll be revising <em>three</em> times as much information as you'll need, because you'll only be asked to answer eight questions in the exam.</p>
	
	<p>If you're interested, you can find the list of questions that I was given here - <a href="resources/wdp_exam_2011.pdf">wdp_exam_2011.pdf</a></p>
	
