	<h3>Overview</h3>
	
	<p>IA is a good module. There's not a whole lot to say about it. Paul Matthews takes the module, he's a great guy, you should follow him on twitter <a href="http://twitter.com/#!/paulusm">@paulusm</a>. You'll have a two hour slot each week in which most weeks you'll undertake a tutorial. You pretty much have to just get on with it, but Paul will of course help if you get stuck. You need to do a minimum number in order to pass, I think it's 6. As well as these practicals, you'll be put into groups and assigned IA topics which you will base a 20 minute presentation on - 20 minutes each, so if you're in a group of three, all of your presentations together should total an hour. Near the end of the second semester you'll have your presentations.</p>
	
	<h3>Key Takeaways</h3>
	
	<p>This module doesn't require as much to be done outside of class, aside from researching your topic, so if you're organised and get the practicals done inside of timetabled hours, you'll give yourselves more time for the more challenging modules.</p>
	
	<p>Don't freak out about the length of the presentation, you'll have loads to say. Do something interactive with the audience to keep them interested.</p>
	
	<h3>Exam</h3>
	
	<p>This exam was a piece of cake. It's multiple choice, and if you've been paying attention you'll do really well.</p>
	
	
