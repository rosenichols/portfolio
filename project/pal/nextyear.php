	<h3>Overview</h3>
	
	<p>You'll meet Nick Plant. I'll leave you to find out about him. You'll probably also meet Peter Rawlings. Same goes. The other person on this module is Paul Hulbert.</p>
	
	<p>At the beginning of the year it'll seem pointless. That won't have changed by the end. That's not to say that you can't get anything from the module though.</p>
	
	<p>A couple of weeks in, maybe a month, you'll be randomly split into groups of around 5 or 6. This module is shared with BABIS and ITMB students so you'll be in with some of each. Once you've been put into these groups, you'll be working together for the rest of the year, so it's worth making an effort to get along. The same day as you're put into your groups, you'll also be assigned a 'host' (for host read client - host is the term used because all of the clients are within the confines of UWE, or something. Anyway, you'll get used to the obscure and confusing business speak).</p>
	
	<p>So, once you've got your host, you go and meet them. They'll want you to improve some aspect of their current information system. I was quite fortunate that my team (group and team will become very familiar terms to you) were assigned a good host in Barney of the drama department at St Matts.</p>
	
	<h3>Coursework</h3>

	<p>What I've already said mostly covers the coursework; the whole thing is kind of the coursework, you'll get a monster spec (must've been 10+ pages) which explains everything you'll have to do. One thing I haven't mentioned is that you'll have to complete logbook entries, three in all, about tasks that you've completed.</p>
	
	<h3>Exam</h3>
	
	<p>Not exactly an exam, but you will have something by way of a final presentation in this module. In it you will essentially lay out what you did, what you learned, that kind of thing. You shouldn't find it too hard. The hardest thing will probably be not losing your will to live, listening to hours of essentially the same presentation over and over. Well, enjoy!</p>
	
	<h3>Key Takeaways</h3>
	
	<ul class="nextyear">
	<li>Organisation is key, do something every week.</li>
	
	<li>One thing my team did which worked well is arrange a time to meet, just for an hour, every week. It sounds like a lot of hassle but it's not really, and it'll really help the team to stay on track.</li>
	
	<li>Get on with your team. You'll only be making life hard for yourself otherwise.</li>
	
	<li>Get on well with the host. They'll be talking to the module leaders about how you did.</li>
	
	<li>Get on well with the module. You'll be doing the whole thing again but more in Year 3's Consultancy Project module, only this time with a real world client.</li>
	</ul>
	
