	<h3>Overview</h3>
	
	<p>The thing that irritated me the most about this module is the hyphen. Seriously, having to hyphen 'Human-Computer' every time gets on my nerves.</p>
	
	<p>Aside from that, I actually enjoyed this module, I found the content really interesting. Not everybody shared my view, however, I'm pretty sure that some on the course thought it was pointless. It depends what you're interested in, I suppose. Unfortunately for me, the two hour lecture was timetabled to start at 9am, and because I commute from Newport it meant I quite often missed the lectures, at least the first hour.</p>
	
	<p>This module deals mostly with usability.</p>
	
	<h3>Coursework</h3>
	
	<p>The coursework for this module is split into four parts.</p>
	
	<p><b>Part 1</b> - Individual. PACT (People Activities Context Technologies) Analysis, much fun, you'll be taught all about how to do this so don't worry. <a href="http://rosenichols.co.uk/images/pact_analysis.png">Take a look at mine (BUT DO NOT COPY)</a>. Additionally, you have to do a piece of work giving examples of visibility, affordances, feedback and mapping. You'll be taught this too.</p>
	
	<p><b>Part 2</b> - Group work. Conduct a co-operative analysis (usability test). Again, you'll be shown exactly how to go about doing this.</p>
	
	<p><b>Part 3</b> - Group work. Presentation based on research you have conducted into touch screen systems. Fun stuff. You have to go into town and test an iPlus machine.</p>
	
	<p><b>Part 4</b> - Individual. Produce a prototype for a touch screen interface. Produce lo-fi and hi-fi prototypes. Do testing. Create personas and scenarios.</p>
	
